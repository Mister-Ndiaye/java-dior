    private void hashButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hashButtonActionPerformed
        // TODO add your handling code here:
        texte = hashToTextArea.getText();
        if (texte.isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Le texte ne doit pas Ãªtre vide", "Error Message", JOptionPane.CLOSED_OPTION, 0);
            return;
        
        }
        
        if (!texte.isEmpty()) {
             
             try {
                    empreinte = Hachage.hashData(texte,algo);
                    hashTextArea.setText(empreinte);
            } catch (Exception ex) {
                Logger.getLogger(HachageFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
                
        } else {
             try {
                empreinte = Hachage.hashFile(path, algo);
                hashTextArea.setText(empreinte);
            } catch (Exception ex) {
                Logger.getLogger(HachageFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
            }
        
        
        
        
        
    }//GEN-LAST:event_hashButtonActionPerformed

    private void fileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileButtonActionPerformed
        // TODO add your handling code here:JFileChooser jFileChooser = new JFileChooser();
        JFileChooser fileChooser = new JFileChooser();
                    int returnValue = fileChooser.showOpenDialog(null);

                    if (returnValue == JFileChooser.APPROVE_OPTION) {
                        File selectedFile = fileChooser.getSelectedFile();

                        try {
                            BufferedReader reader = new BufferedReader(new FileReader(selectedFile));
                            StringBuilder content = new StringBuilder();
                            String line;

                            while ((line = reader.readLine()) != null) {
                                content.append(line).append("\n");
                            }

                            reader.close();
                            hashToTextArea.setText(content.toString());
                        } catch (IOException ex) {
                            ex.printStackTrace();
                            JOptionPane.showMessageDialog(null, "Error reading file: " + ex.getMessage());
                        }
                    }
                
        
        
        
    }//GEN-LAST:event_fileButtonActionPerformed

